    private void jbt_luuActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbt_luuActionPerformed
        // TODO add your handling code here:
        DataConnection con =new DataConnection();
        Connection c=con.getConnection();
        PreparedStatement pStmt=null;
        try{
            pStmt=c.prepareStatement("insert into donhang values(?,?,?)");
            pStmt.setString(1,jtf_madh.getText());
            pStmt.setString(2,jtf_makh.getText());
            pStmt.setString(3,jtf_ngaylap.getText());
            pStmt.executeUpdate();
            int a=jtb_ctdh.getRowCount();
            for(int i=0;i<a;i++){
               pStmt=null;
               try{
               pStmt=c.prepareStatement("insert into chitietdonhang values(?,?,?,?);");
               pStmt.setString(1,jtf_madh.getText());
               pStmt.setString(2, (String) jtb_ctdh.getValueAt(i,0));
               pStmt.setString(3, (String) jtb_ctdh.getValueAt(i,2));
               pStmt.setString(4, (String) jtb_ctdh.getValueAt(i,3));
               pStmt.executeUpdate();
               }catch(SQLException ex){
                   JOptionPane.showMessageDialog(null,"Lỗi!");
               }
            }
            JOptionPane.showMessageDialog(null,"Thành công!");
        }catch(SQLException ex){
            JOptionPane.showMessageDialog(null,"Lỗi!");
    }
    }//GEN-LAST:event_jbt_luuActionPerformed

