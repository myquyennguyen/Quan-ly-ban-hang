    private void jbt_themActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbt_themActionPerformed
        // TODO add your handling code here:
        DataConnection con=new DataConnection();
        Connection c=con.getConnection();
        PreparedStatement pStmt=null;
        ResultSet rs=null;
        try{
            pStmt=c.prepareStatement("select Ten_HH,Gia from hanghoa where Ma_HH=?");
            pStmt.setString(1,jtf_mahh.getText());
            rs=pStmt.executeQuery();
            DefaultTableModel model = (DefaultTableModel) jtb_ctdh.getModel();
            while(rs.next()){
                Vector vector=new Vector();
                vector.add(jtf_mahh.getText());
                vector.add(rs.getString("Ten_HH"));
                vector.add(jtf_solg.getText());
                vector.add(rs.getString("Gia"));
                model.addRow(vector);
            }
            jtf_mahh.setText(null);
            jtf_solg.setText(null);
        }catch(SQLException ex){
            JOptionPane.showMessageDialog(null,"Lỗi!");
        }
    }//GEN-LAST:event_jbt_themActionPerformed

    private void jbt_hoantatActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbt_hoantatActionPerformed
        // TODO add your handling code here:
        DataConnection con=new DataConnection();
        Connection c=con.getConnection();
        PreparedStatement pStmt=null;
        ResultSet rs=null,rs2=null;
        try{
            pStmt=c.prepareStatement("select * from khachhang where Ma_KH=?");
            pStmt.setString(1,jtf_makh.getText());
            rs=pStmt.executeQuery();
            rs.next();
            jtf_hoten.setText(rs.getString("Ho_Ten"));
            jtf_dc.setText(rs.getString("Dia_Chi_KH"));
            jtf_sdt.setText(rs.getString("So_DT_KH"));
        } catch(SQLException ex){
            JOptionPane.showMessageDialog(null,"Lỗi!");
        }
        int count=jtb_ctdh.getRowCount();
        int tong=0;
        for(int i=0;i<count;i++){
            tong=tong+(Integer.parseInt((String) jtb_ctdh.getValueAt(i,2)) * Integer.parseInt((String) jtb_ctdh.getValueAt(i,3)));
        }
        jtf_tongtien.setText(String.valueOf(tong));
    }//GEN-LAST:event_jbt_hoantatActionPerformed

